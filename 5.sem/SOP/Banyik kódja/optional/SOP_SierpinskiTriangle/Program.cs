4. Jelenlegi üzleti folyamatok modellje
Az adminisztráció manuális és időigényes folyamatokon alapul, 
amelyek során a tanárok és diákok adatait kézzel kell beírni a rendszerbe.
Az adatok közötti szinkronizáció hiánya miatt gyakran előfordulnak 
hibák és eltérések a különböző adatforrások között.
A jelenlegi rendszer nem biztosít hatékony lekérdezési lehetőségeket, 
nehezen követhető és nehezen értelmezhető statisztikákat eredményez.
5. Igényelt üzleti folyamatok modellje
Az új rendszernek automatizálnia kell az adminisztrációs feladatokat, 
felszabadítva ezzel az emberi erőforrásokat.
A redundanciák elkerülése és a pontos adatok garantálása érdekében az 
adatokat egyszer kell felvinni.
Az új rendszernek lehetővé kell tennie a hatékony lekérdezéseket és 
statisztikák készítését az adminisztráció számára.
6. Használati esetek
A weboldalt tanárok és diákok egyaránt használják az adminisztratív 
feladatok, adatok nyilvántartása és lekérdezése céljából.
7. Megfeleltetés, hogyan fedik le a használati esetek a követelményeket
A tanárok az új weboldalt használva könnyen fel tudják vinni és 
karbantartani az adatokat, valamint hatékonyan lekérdezhetik azokat, 
hogy nyomon kövessék a diákok fejlődését és az oktatási folyamatot.
