10. Funkció-követelmény megfeleltetés
Automatizált adatfeltöltés: A rendszer automatikusan érzékeli az 
adatokat és frissíti azokat.
Hatékony lekérdezések: A felhasználók könnyen lekérdezhetik a diákok 
teljesítményét és az adminisztratív adatokat.
Adatmegosztás: Tanárok és diákok közötti adatmegosztás a felhasználói 
jogosultságok figyelembevételével.
11. Fogalomszótár
Adatmegosztás: A rendszer képessége adatok megosztására a felhasználói 
jogosultságoknak megfelelően.
Automatizált adatfeltöltés: A funkció, amely automatikusan frissíti az adatokat.
Hatékony lekérdezések: A rendszer képessége gyors és pontos lekérdezésekre.